-- 14: nth Fibonacci number (1-indexed: fn_fib(1)=0, fn_fib(2)=1)
CREATE OR REPLACE FUNCTION fn_fibonacci (p_n IN PLS_INTEGER) RETURN NUMBER IS
  a NUMBER := 0;
  b NUMBER := 1;
  temp NUMBER;
BEGIN
  IF p_n <= 0 THEN
    RETURN NULL;
  ELSIF p_n = 1 THEN
    RETURN a;
  ELSIF p_n = 2 THEN
    RETURN b;
  ELSE
    FOR i IN 3 .. p_n LOOP
      temp := a + b;
      a := b;
      b := temp;
    END LOOP;
    RETURN b;
  END IF;
END;
