CREATE OR REPLACE FUNCTION fn_factorial (p_n IN PLS_INTEGER) RETURN NUMBER IS
  v_fact NUMBER := 1;
BEGIN
  IF p_n < 0 THEN
    RAISE_APPLICATION_ERROR(-20001, 'Negative number not allowed');
