CREATE OR REPLACE TRIGGER trg_maintain_rowcount
AFTER INSERT OR DELETE ON emp
DECLARE
  v_cnt NUMBER;
BEGIN
  SELECT COUNT(*) INTO v_cnt FROM emp;
  MERGE INTO row_count_table r USING (SELECT 'EMP' tbl FROM dual) src
  ON (r.tbl_name = src.tbl)
